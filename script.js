// Add some market data simulation (for demo purposes)
function updateMarketData() {
    const priceElements = document.querySelectorAll('.pair-price');
    priceElements.forEach(element => {
        const currentPrice = parseFloat(element.textContent);
        const change = (Math.random() - 0.5) * 0.001;
        const newPrice = currentPrice + change;
        element.textContent = newPrice.toFixed(4);
        
        // Add color animation
        element.style.color = change > 0 ? '#00b894' : '#ff6b35';
        setTimeout(() => {
            element.style.color = '#ffffff';
        }, 1000);
    });
}

